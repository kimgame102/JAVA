package castingTest;

public class CastingTest2 {
	public static void main(String[] args) {
//		자동 형변환
		char data = 48;
		System.out.println(data + 10); // 문자 + 정수 = 정수
		System.out.println('A' + 10);
		System.out.println("=========================");
		
//		강제 형변환
		System.out.println((char)50);
	}
}